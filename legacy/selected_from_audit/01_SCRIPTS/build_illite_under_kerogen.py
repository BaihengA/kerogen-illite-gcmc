"""
Build an illite mineral substrate under rough kerogen plates.

Inputs expected in project root:
  00_INPUT/Illite*.gro or 00_INPUT/Illite*.pdb
  00_INPUT/Illite*.itp optional but strongly recommended
  02_GRAPHENE_CASES/RMS_xxx/02_RUN/STANDARD_KEROGEN_PLATE/RMS_xxx_standard_kerogen_plate.gro

Outputs:
  ILLITE_UNDER_KEROGEN/RMS_xxx/RMS_xxx_illite_under_kerogen.gro
  ILLITE_UNDER_KEROGEN/RMS_xxx/RMS_xxx_ILLITE_SUPERCELL.itp
  reports and helper files

This is a geometry/topology helper. Always validate force-field parameters before production MD.
"""
